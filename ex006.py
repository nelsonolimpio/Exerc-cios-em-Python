"Pedir um valor para o usuário. E imprimir o dobro do valor digitado, o triplo do valor digitado e a raiz quadrada do valor digitado"

num = int(input('Digite um número:'))

print('Valor digitado = {}, Dobro do valor digitado= {}, Triplo do valor digitado = {} e Raiz quadrada do valor digitado = {}'.format(num, (num*2), (num*3), (num**(1/2))))
